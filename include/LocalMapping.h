#ifndef LOCALMAPPING_H
#define LOCALMAPPING_H

#include "KeyFrame.h"
#include "Map.h"
#include "LoopClosing.h"
#include "Tracking.h"
#include "KeyFrameDatabase.h"

#include <mutex>


namespace PLVS
{

class Tracking;
class LoopClosing;
class Map;

class LocalMapping
{
public:
    LocalMapping(Map* pMap, const float bMonocular);

    void SetLoopCloser(LoopClosing* pLoopCloser);

    void SetTracker(Tracking* pTracker);

    // Main function
    void Run();

    void InsertKeyFrame(KeyFramePtr pKF);

    // Thread Synch
    void RequestStop();
    void RequestReset();
    bool Stop();
    void Release();
    bool isStopped();
    bool stopRequested();
    bool AcceptKeyFrames();
    void SetAcceptKeyFrames(bool flag);
    bool SetNotStop(bool flag);

    void InterruptBA();

    void RequestFinish();
    bool isFinished();

    int KeyframesInQueue(){
        unique_lock<std::mutex> lock(mMutexNewKFs);
        return mlNewKeyFrames.size();
    }

    void AddNewKeyFramesToSet(std::set<KeyFramePtr>& set);
        
protected:

    bool CheckNewKeyFrames();
    void ProcessNewKeyFrame();
    void CreateNewMapFeatures();
    
    void MapPointCulling();
    
    void MapLineCulling();
    
    void SearchInNeighbors();

    void KeyFrameCulling();

    cv::Mat ComputeF12(KeyFramePtr& pKF1, KeyFramePtr& pKF2);
    void ComputeF12_H12(KeyFramePtr& pKF1, KeyFramePtr& pKF2, cv::Mat& F12, cv::Mat& H12, cv::Mat& e1);  
    void ComputeF12_H21(KeyFramePtr& pKF1, KeyFramePtr& pKF2, cv::Mat& F12, cv::Mat& H21, cv::Mat& e2);      

    cv::Mat SkewSymmetricMatrix(const cv::Mat &v);

    bool mbMonocular;

    void ResetIfRequested();
    bool mbResetRequested;
    std::mutex mMutexReset;

    bool CheckFinish();
    void SetFinish();
    bool mbFinishRequested;
    bool mbFinished;
    std::mutex mMutexFinish;

    Map* mpMap;

    LoopClosing* mpLoopCloser;
    Tracking* mpTracker;

    std::list<KeyFramePtr> mlNewKeyFrames;

    KeyFramePtr mpCurrentKeyFrame;

    std::list<MapPointPtr> mlpRecentAddedMapPoints;
    std::list<MapLinePtr> mlpRecentAddedMapLines;

    std::mutex mMutexNewKFs;

    bool mbAbortBA;

    bool mbStopped;
    bool mbStopRequested;
    bool mbNotStop;
    std::mutex mMutexStop;

    bool mbAcceptKeyFrames;
    std::mutex mMutexAccept;
    
};

} //namespace PLVS

#endif // LOCALMAPPING_H
